package com.restapplication.service;

//hold user information data
public class Profile{
	
			String location;
			String description;
			String screen_name;
			String name;
			
			public String getLocation() {
				return location;
			}
			public String getDescription() {
				return description;
			}
			public String getScreenName() {
				return screen_name;
			}
			public String getName() {
				return name;
			}

}
